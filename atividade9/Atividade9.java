package atividade9;

public abstract class Atividade9 {

		double somar(double n1,double n2) {
		double somar = n1 + n2;
		return somar;
		}
		double subtrair(double n1,double n2) {
			double subtrair = n1 - n2;
		return subtrair;
		}
		double multiplicar(double n1,double n2) {
			double multiplicar = n1 * n2;
			return multiplicar;
		}
		double dividir(double n1,double n2) {
			double dividir = n1 / n2;
			return dividir;
		}
	}


